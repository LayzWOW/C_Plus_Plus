#include <iostream>

using namespace std;

int main() {
      int integerType;
      float floatType;
      double doubleType;
      char charType;

      cout << "Size of integerType is: " << sizeof(integerType) << "\n";
      cout << "Size of floatType is: " << sizeof(floatType) << "\n";
      cout << "Size of doubleType is: " << sizeof(doubleType) << "\n";
      cout << "Size of charType is: " << sizeof(charType) << "\n";

      return 0;
}
